package com.composite.room;

public class TypeLamp {
	
	private String type;
	private String position;
	
	public TypeLamp(String type, String position) {
		super();
		this.type = type;
		this.position = position;
	}

	public String getType() {
		return type;
	}

	public String getPosition() {
		return position;
	}
	
	
	
	
		
	

}
